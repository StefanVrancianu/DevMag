#import "UIColor+MyColor.h"

@implementation UIColor (MyColor)

+(UIColor *) myRed {
    
    return [UIColor colorWithRed:1 green:0 blue:0 alpha:0.4];
    
}

+(UIColor *) myBlue {
    
    return [UIColor colorWithRed:0 green:0 blue:1 alpha:0.4];
    
}

@end
